const Mongoose = require("mongoose");

const UserSchema = new Mongoose.Schema({
  firstname: {
    type: String,
    required: true,
  },
  lastname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    unique: true,
    required: true,
  },
  password: {
    type: String,
    minlength: 6,
    required: true,
  },
  role: {
    type: String,
    default: "customer",
  },
});


const User = Mongoose.model("User", UserSchema);
module.exports = User;
